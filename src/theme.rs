static ALL_THEMES: &[&Theme] = &[
    &TOKYO_NIGHT,
    &CATPPUCCIN_MOCHA,
    &CATPPUCCIN_LATTE,
    &DRACULA,
    &GRUVBOX_DARK,
    &NORD,
    &SOLARIZED_LIGHT,
    &ROSE_PINE_DAWN,
];

pub fn by_name(name: &str) -> &'static Theme {
    let normalized = name.to_lowercase().replace(['_', ' '], "-");
    for theme in ALL_THEMES {
        if theme.name == normalized {
            return theme;
        }
    }
    // Check aliases
    match normalized.as_str() {
        "tokyonight" => &TOKYO_NIGHT,
        "catppuccin" => &CATPPUCCIN_MOCHA,
        "latte" => &CATPPUCCIN_LATTE,
        "gruvbox" => &GRUVBOX_DARK,
        "solarized" => &SOLARIZED_LIGHT,
        "rose-pine" | "rosepine" => &ROSE_PINE_DAWN,
        _ => &TOKYO_NIGHT,
    }
}
